#Listing favorite foods.
Favorite_Foods = ['Tacos','Ramen','Pasta','Pizza','Gyro']
print(Favorite_Foods)

# First Item will be printed which is Tacos.
print(Favorite_Foods[0])

# Last Item will be printed which is Gyro.
print(Favorite_Foods[-1])

